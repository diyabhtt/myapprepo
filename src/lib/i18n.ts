type UiTranslationKey = keyof typeof UI_TRANSLATIONS['English']

function applyTemplate(template: string, variables: Record<string, string | number>): string {
  return Object.entries(variables).reduce(
    (current, [key, value]) => current.replaceAll(`{{${key}}}`, String(value)),
    template,
  )
}

export function translateStatusLabel(statusLabel: string, language: SupportedLanguage): string {
  return STATUS_TRANSLATIONS[statusLabel]?.[language] ?? STATUS_TRANSLATIONS[statusLabel]?.English ?? statusLabel
}

export function translateUi(
  language: SupportedLanguage,
  key: UiTranslationKey,
  variables: Record<string, string | number> = {},
): string {
  const translationMap = UI_TRANSLATIONS as Partial<Record<SupportedLanguage, Partial<Record<UiTranslationKey, string>>>>
  const template = translationMap[language]?.[key] ?? UI_TRANSLATIONS.English[key]
  return applyTemplate(template, variables)
}

export function callSpeakerLabel(speaker: CallSpeaker, language: SupportedLanguage): string {
  if (speaker === 'assistant') return translateUi(language, 'speakerAssistant')
  if (speaker === 'member') return translateUi(language, 'speakerMember')
  return translateUi(language, 'speakerSystem')
}

